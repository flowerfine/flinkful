package cn.sliew.flinkful.kubernetes.common.dict;

import cn.sliew.carp.framework.common.dict.DictInstance;
import com.baomidou.mybatisplus.annotation.EnumValue;
import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonFormat;
import lombok.Getter;
import lombok.RequiredArgsConstructor;

import java.util.Arrays;

@Getter
@RequiredArgsConstructor
@JsonFormat(shape = JsonFormat.Shape.OBJECT)
public enum FlinkSemantic implements DictInstance {

    EXACTLY_ONCE("exactly_once", "精确一次"),
    AT_LEAST_ONCE("at_least_once", "至少一次"),
    NONE("none", "无"),
    ;

    @JsonCreator
    public static FlinkSemantic of(String value) {
        return Arrays.stream(values())
                .filter(instance -> instance.getValue().equals(value))
                .findAny().orElseThrow(() -> new EnumConstantNotPresentException(FlinkSemantic.class, value));
    }

    @EnumValue
    private final String value;
    private final String label;
}
